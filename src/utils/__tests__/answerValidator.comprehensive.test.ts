import { describe, it, expect } from 'vitest';
import {
  isMultipleChoice,
  checkAnswer,
  canSelectMoreAnswers,
  getCorrectAnswersCount,
  getAnswerHint,
} from '../answerValidator';

// ============================================================================
// isMultipleChoice
// ============================================================================

describe('isMultipleChoice', () => {
  describe('должен возвращать false для одиночного выбора', () => {
    it('должен возвращать false для одного правильного ответа [0]', () => {
      expect(isMultipleChoice([0])).toBe(false);
    });

    it('должен возвращать false для одного правильного ответа [1]', () => {
      expect(isMultipleChoice([1])).toBe(false);
    });

    it('должен возвращать false для одного правильного ответа [2]', () => {
      expect(isMultipleChoice([2])).toBe(false);
    });

    it('должен возвращать false для одного правильного ответа [3]', () => {
      expect(isMultipleChoice([3])).toBe(false);
    });

    it('должен возвращать false для пустого массива', () => {
      expect(isMultipleChoice([])).toBe(false);
    });
  });

  describe('должен возвращать true для множественного выбора', () => {
    it('должен возвращать true для двух правильных ответов [0, 1]', () => {
      expect(isMultipleChoice([0, 1])).toBe(true);
    });

    it('должен возвращать true для двух правильных ответов [0, 2]', () => {
      expect(isMultipleChoice([0, 2])).toBe(true);
    });

    it('должен возвращать true для трёх правильных ответов [0, 1, 2]', () => {
      expect(isMultipleChoice([0, 1, 2])).toBe(true);
    });

    it('должен возвращать true для четырёх правильных ответов [0, 1, 2, 3]', () => {
      expect(isMultipleChoice([0, 1, 2, 3])).toBe(true);
    });

    it('должен возвращать true для несортированных ответов [2, 0]', () => {
      expect(isMultipleChoice([2, 0])).toBe(true);
    });
  });
});

// ============================================================================
// checkAnswer - Одиночный выбор
// ============================================================================

describe('checkAnswer - Одиночный выбор', () => {
  describe('должен возвращать true для правильных ответов', () => {
    it('должен возвращать true когда пользователь выбрал [0] и правильный [0]', () => {
      expect(checkAnswer([0], [0])).toBe(true);
    });

    it('должен возвращать true когда пользователь выбрал [1] и правильный [1]', () => {
      expect(checkAnswer([1], [1])).toBe(true);
    });

    it('должен возвращать true когда пользователь выбрал [2] и правильный [2]', () => {
      expect(checkAnswer([2], [2])).toBe(true);
    });

    it('должен возвращать true когда пользователь выбрал [3] и правильный [3]', () => {
      expect(checkAnswer([3], [3])).toBe(true);
    });

    it('должен возвращать true когда пользователь выбрал число 0 и правильный [0]', () => {
      expect(checkAnswer(0, [0])).toBe(true);
    });

    it('должен возвращать true когда пользователь выбрал число 2 и правильный [2]', () => {
      expect(checkAnswer(2, [2])).toBe(true);
    });
  });

  describe('должен возвращать false для неправильных ответов', () => {
    it('должен возвращать false когда пользователь выбрал [0] а правильный [1]', () => {
      expect(checkAnswer([0], [1])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал [1] а правильный [0]', () => {
      expect(checkAnswer([1], [0])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал [2] а правильный [3]', () => {
      expect(checkAnswer([2], [3])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал [3] а правильный [0]', () => {
      expect(checkAnswer([3], [0])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал число 1 а правильный [0]', () => {
      expect(checkAnswer(1, [0])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал число 0 а правильный [2]', () => {
      expect(checkAnswer(0, [2])).toBe(false);
    });
  });

  describe('должен возвращать false для пустых и null ответов', () => {
    it('должен возвращать false когда пользователь не ответил (null)', () => {
      expect(checkAnswer(null as any, [0])).toBe(false);
    });

    it('должен возвращать false когда пользователь не ответил (undefined)', () => {
      expect(checkAnswer(undefined as any, [0])).toBe(false);
    });

    it('должен возвращать false когда пользователь выбрал пустой массив', () => {
      expect(checkAnswer([], [0])).toBe(false);
    });
  });
});

