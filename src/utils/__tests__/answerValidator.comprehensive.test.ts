describe('canSelectMoreAnswers', () => {
  describe('должен возвращать true когда можно выбрать ещё', () => {
    it('должен возвращать true когда выбрано 0 из 2', () => {
      expect(canSelectMoreAnswers([], [0, 1])).toBe(true);
    });

    it('должен возвращать true когда выбран 1 из 2', () => {
      expect(canSelectMoreAnswers([0], [0, 1])).toBe(true);
    });

    it('должен возвращать true когда выбран 1 из 3', () => {
      expect(canSelectMoreAnswers([0], [0, 1, 2])).toBe(true);
    });

    it('должен возвращать true когда выбрано 2 из 3', () => {
      expect(canSelectMoreAnswers([0, 1], [0, 1, 2])).toBe(true);
    });

    it('должен возвращать true когда выбрано 0 из 4', () => {
      expect(canSelectMoreAnswers([], [0, 1, 2, 3])).toBe(true);
    });
  });

  describe('должен возвращать false когда нельзя выбрать ещё', () => {
    it('должен возвращать false когда выбраны все 2 из 2', () => {
      expect(canSelectMoreAnswers([0, 1], [0, 1])).toBe(false);
    });

    it('должен возвращать false когда выбраны все 3 из 3', () => {
      expect(canSelectMoreAnswers([0, 1, 2], [0, 1, 2])).toBe(false);
    });

    it('должен возвращать false когда выбраны все 4 из 4', () => {
      expect(canSelectMoreAnswers([0, 1, 2, 3], [0, 1, 2, 3])).toBe(false);
    });

    it('должен возвращать false когда выбрано больше чем нужно', () => {
      expect(canSelectMoreAnswers([0, 1, 2], [0, 1])).toBe(false);
    });
  });
});

