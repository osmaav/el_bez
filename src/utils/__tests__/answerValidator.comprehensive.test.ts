describe('getAnswerHint', () => {
  describe('должен возвращать подсказку для одиночного выбора', () => {
    it('должен возвращать "Выберите один правильный вариант" для [0]', () => {
      expect(getAnswerHint([0])).toBe('Выберите один правильный вариант');
    });

    it('должен возвращать "Выберите один правильный вариант" для [2]', () => {
      expect(getAnswerHint([2])).toBe('Выберите один правильный вариант');
    });
  });

  describe('должен возвращать подсказку для 2-4 правильных ответов', () => {
    it('должен возвращать "Выберите 2 правильных варианта" для [0, 1]', () => {
      expect(getAnswerHint([0, 1])).toBe('Выберите 2 правильных варианта');
    });

    it('должен возвращать "Выберите 2 правильных варианта" для [0, 2]', () => {
      expect(getAnswerHint([0, 2])).toBe('Выберите 2 правильных варианта');
    });

    it('должен возвращать "Выберите 3 правильных варианта" для [0, 1, 2]', () => {
      expect(getAnswerHint([0, 1, 2])).toBe('Выберите 3 правильных варианта');
    });

    it('должен возвращать "Выберите 4 правильных варианта" для [0, 1, 2, 3]', () => {
      expect(getAnswerHint([0, 1, 2, 3])).toBe('Выберите 4 правильных варианта');
    });
  });

  describe('должен возвращать подсказку для 5+ правильных ответов', () => {
    it('должен возвращать "Выберите все правильные варианты (5)" для 5 ответов', () => {
      expect(getAnswerHint([0, 1, 2, 3, 4])).toBe('Выберите все правильные варианты (5)');
    });

    it('должен возвращать "Выберите все правильные варианты (6)" для 6 ответов', () => {
      expect(getAnswerHint([0, 1, 2, 3, 4, 5])).toBe('Выберите все правильные варианты (6)');
    });
  });
});
